package org.JSON;

public class SY {
	private String MC;
	private int BH;
	private double BQ;
	private double LJ;
	public String getMC() {
		return MC;
	}
	public void setMC(String mC) {
		MC = mC;
	}
	public int getBH() {
		return BH;
	}
	public void setBH(int bH) {
		BH = bH;
	}
	public double getBQ() {
		return BQ;
	}
	public void setBQ(double bQ) {
		BQ = bQ;
	}
	public double getLJ() {
		return LJ;
	}
	public void setLJ(double lJ) {
		LJ = lJ;
	}
}
