package org.JSON;

public class TS {
	private String MC;
	private int BH;
	private double AYE;
	private double BYE;
	private double hj;
	public String getMC() {
		return MC;
	}
	public void setMC(String mC) {
		MC = mC;
	}
	public int getBH() {
		return BH;
	}
	public void setBH(int bH) {
		BH = bH;
	}
	public double getAYE() {
		return AYE;
	}
	public void setAYE(double aYE) {
		AYE = aYE;
	}
	public double getBYE() {
		return BYE;
	}
	public void setBYE(double bYE) {
		BYE = bYE;
	}
	public double getHj() {
		return hj;
	}
	public void setHj(double hj) {
		this.hj = hj;
	}
}
